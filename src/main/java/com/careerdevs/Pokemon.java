package com.careerdevs;

public class Pokemon {
    private int id;
    private String name;
    private String types;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTyping() {
        return types;
    }

    public void setTyping(String typing) {
        this.types = typing;
    }
}
